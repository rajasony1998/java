import java.util.*;
import java.lang.*;
public class Factorial{
      public static void main(String args[]){
           int f=1;
           Scanner s=new Scanner(System.in);
            int n=sc.nextInt();
           for(int i=1;i<=n;i++){
               f=f*i;
               
           }
System.out.println(f);
}
}
